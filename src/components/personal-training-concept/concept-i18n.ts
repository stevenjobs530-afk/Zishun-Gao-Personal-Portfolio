export function translateConceptText(value: string, language: ConceptLanguage) {
  if (language === "en") return value;
  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  const trimmed = value.trim();
  const exact = zh[trimmed];
  if (exact) return `${leading}${exact}${trailing}`;
  for (const [pattern, replacement] of dynamicTranslations) {
    if (!pattern.test(trimmed)) continue;
    const translated = trimmed.replace(pattern, replacement as (substring: string, ...args: string[]) => string);
    return `${leading}${translated}${trailing}`;
  }
  return value;
}
